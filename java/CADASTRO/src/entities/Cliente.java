package entities;

public class Cliente {

	
	public String nome;
	public String cpf;
	public int nascimento;
	public String estadoCivil;
	public String nacionalidade;
	
	
	


	public int idade() {
		
		return 2021-nascimento;
	}
@Override
	public String toString() {
		return "Cliente [nome=" + nome + ", cpf=" + cpf + ", nascimento=" + nascimento + ", estadoCivil=" + estadoCivil
				+ ", nacionalidade=" + nacionalidade + ", idade()=" + idade() + "]";
	}

}
